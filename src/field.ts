export class Field {
  field;
  size;

  constructor(size) {
    this.field = this.readField();
    this.size = size;
  }

  readField() {
    let buf = [];
    field.forEach((row, y) => {
      buf.push([]);
      for (let x = 0; x < row.length; x++) {
        buf[y].push(+row[x]);
      }
    });
    return buf;
  }

  getCell(x, y) {
    const size = this.size;
    return this.field[Math.floor(y / size)][Math.floor(x / size)];
  }
}